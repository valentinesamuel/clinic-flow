// Helper function to get LGAs for a given state
export const getLGAsForState = (stateValue: string): LGAOption[] => {
  return lgasByState[stateValue] || [];
};

// Helper to find state by value
export const findStateByValue = (value: string): StateOption | undefined => {
  return nigerianStates.find(state => state.value === value);
};

// Helper to find LGA by value within a state
export const findLGAByValue = (stateValue: string, lgaValue: string): LGAOption | undefined => {
  const lgas = lgasByState[stateValue] || [];
  return lgas.find(lga => lga.value === lgaValue);
};
